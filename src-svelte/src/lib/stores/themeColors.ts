export function getThemeColors(theme: string): ThemeColors {
  const colors = themeColors[theme];
  if (!colors) {
    return themeColors['dark']!;
  }
  const dark = themeColors['dark']!;
  if (Object.keys(colors.weechatForeground).length === 0 && Object.keys(colors.weechatBackground).length === 0) {
    return {
      ...colors,
      weechatForeground: dark.weechatForeground,
      weechatBackground: dark.weechatBackground,
    };
  }
  return colors;
}
